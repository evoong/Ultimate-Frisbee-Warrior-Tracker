-- ============================================================
-- 010_league_tracking.sql
-- League tracking: opponents as entities, full league schedule,
-- auto-computed standings inputs, playoff bracket structure.
--
-- Model:
-- - league_teams: one row per team per season, including our own
--   team (flagged is_us, at most one per season). Opponent notes
--   (scouting) live here.
-- - league_games: the canonical league schedule and bracket. Every
--   matchup in the league is a row, including our own games. A
--   matchup we play in links to its rich games row via our_game_id
--   and stores NO score of its own: the app derives the score from
--   that game's goal events (single source of truth). Matchups we
--   are not in store home_score/away_score directly and are marked
--   final by setting status.
-- - Playoff games carry round/bracket_pos plus next_game_id and
--   next_slot ('home'|'away') pointers; the app writes the winner
--   into the next game's open slot when a result is recorded.
-- - seasons gain win_points/tie_points/loss_points (default 2/1/0)
--   so standings math is per-league configurable.
-- - games.opponent_team_id links a game to its league team. The
--   legacy games.opponent text column remains the way opponents are
--   entered; triggers below resolve it to a league_teams row and keep
--   the paired league_games row in sync on every insert/update/delete,
--   so every creation path (app, Jam calendar sync, manual SQL) upholds
--   the pairing invariant with no application plumbing.
-- - The old public.standings table is deprecated: nothing reads or
--   writes it. Standings are computed from league_games.
--
-- Run this entire file in the Supabase SQL Editor AFTER 009.
-- Re-runnable (create table if not exists, drop policy if exists).
-- ============================================================

-- ---------- Tables ----------

create table if not exists public.league_teams (
  id bigint generated by default as identity primary key,
  season_id bigint not null references public.seasons(id) on delete cascade,
  name text not null,
  is_us boolean not null default false,
  color text,
  notes text,
  created_at timestamptz not null default now(),
  unique (season_id, name)
);

-- At most one "us" row per season.
create unique index if not exists league_teams_one_us_per_season
  on public.league_teams (season_id) where is_us;

create table if not exists public.league_games (
  id bigint generated by default as identity primary key,
  season_id bigint not null references public.seasons(id) on delete cascade,
  home_team_id bigint references public.league_teams(id) on delete set null,
  away_team_id bigint references public.league_teams(id) on delete set null,
  home_score integer,
  away_score integer,
  game_date date,
  game_time time,
  location text,
  stage text not null default 'regular' check (stage in ('regular', 'playoff')),
  round text,
  bracket_pos integer,
  next_game_id bigint references public.league_games(id) on delete set null,
  next_slot text check (next_slot in ('home', 'away')),
  our_game_id bigint unique references public.games(id) on delete set null,
  status text not null default 'scheduled' check (status in ('scheduled', 'final')),
  created_at timestamptz not null default now()
);

create index if not exists league_games_season_idx on public.league_games (season_id);

alter table public.games
  add column if not exists opponent_team_id bigint references public.league_teams(id) on delete set null;

alter table public.seasons
  add column if not exists win_points integer not null default 2,
  add column if not exists tie_points integer not null default 1,
  add column if not exists loss_points integer not null default 0;

