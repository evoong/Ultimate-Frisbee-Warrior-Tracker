-- ---------- Backfill ----------
-- 1. One is_us league_team per season that has games, named after the
--    season's team (falls back to 'Warriors' if the join is empty).
insert into public.league_teams (season_id, name, is_us)
select s.id, coalesce(t.name, 'Warriors'), true
from public.seasons s
left join public.teams t on t.id = s.team_id
where exists (select 1 from public.games g where g.season_id = s.id)
  and not exists (
    select 1 from public.league_teams lt
    where lt.season_id = s.id and lt.is_us
  );

-- 2. One league_team per distinct opponent name per season.
insert into public.league_teams (season_id, name)
select distinct g.season_id, trim(g.opponent)
from public.games g
where g.season_id is not null
  and g.opponent is not null
  and trim(g.opponent) <> ''
on conflict (season_id, name) do nothing;

-- 3. Link games to their opponent league_team by name.
update public.games g
set opponent_team_id = lt.id
from public.league_teams lt
where g.opponent_team_id is null
  and lt.season_id = g.season_id
  and lt.is_us = false
  and lt.name = trim(g.opponent);

-- 4. A paired league_games row for every existing game (home = us).
--    Scores stay null: the app derives them from the linked game.
insert into public.league_games
  (season_id, home_team_id, away_team_id, game_date, game_time, stage, our_game_id)
select
  g.season_id,
  us.id,
  g.opponent_team_id,
  g.game_date,
  g.game_time,
  case when g.game_type ilike 'playoff%' then 'playoff' else 'regular' end,
  g.id
from public.games g
join public.league_teams us on us.season_id = g.season_id and us.is_us
where g.season_id is not null
  and not exists (
    select 1 from public.league_games lg where lg.our_game_id = g.id
  );

-- ---------- Triggers: games to league_games pairing ----------
-- Created AFTER the backfill so it does not fire mid-backfill.

-- Resolve the opponent text to a league_teams row (creating it on first
-- mention) before a game is written.
create or replace function public.resolve_game_opponent_team()
returns trigger
language plpgsql
as $$
declare
  v_name text := nullif(trim(coalesce(new.opponent, '')), '');
begin
  if new.season_id is null or v_name is null then
    return new;
  end if;
  -- Keep an explicit assignment if it already points at a team in this
  -- season; otherwise re-resolve from the opponent text.
  if new.opponent_team_id is not null and exists (
    select 1 from public.league_teams lt
    where lt.id = new.opponent_team_id and lt.season_id = new.season_id
  ) then
    return new;
  end if;
  insert into public.league_teams (season_id, name)
  values (new.season_id, v_name)
  on conflict (season_id, name) do nothing;
  select id into new.opponent_team_id
  from public.league_teams
  where season_id = new.season_id and name = v_name;
  return new;
end
$$;

-- Maintain the paired league_games row. Idempotent upsert-by-our_game_id,
-- so it self-heals a missing pair on the next touch of the game.
create or replace function public.sync_game_league_pair()
returns trigger
language plpgsql
as $$
declare
  v_us bigint;
  v_stage text;
begin
  if tg_op = 'DELETE' then
    delete from public.league_games where our_game_id = old.id;
    return old;
  end if;
  if new.season_id is null then
    delete from public.league_games where our_game_id = new.id;
    return new;
  end if;

  -- Ensure our own league team exists for this season.
  insert into public.league_teams (season_id, name, is_us)
  select s.id, coalesce(t.name, 'Warriors'), true
  from public.seasons s
  left join public.teams t on t.id = s.team_id
  where s.id = new.season_id
  on conflict do nothing;

  select id into v_us
  from public.league_teams
  where season_id = new.season_id and is_us;

  v_stage := case when new.game_type ilike 'playoff%' then 'playoff' else 'regular' end;

  update public.league_games set
    season_id = new.season_id,
    home_team_id = v_us,
    away_team_id = new.opponent_team_id,
    game_date = new.game_date,
    game_time = new.game_time,
    stage = v_stage
  where our_game_id = new.id;
  if not found then
    insert into public.league_games
      (season_id, home_team_id, away_team_id, game_date, game_time, stage, our_game_id)
    values
      (new.season_id, v_us, new.opponent_team_id, new.game_date, new.game_time, v_stage, new.id);
  end if;
  return new;
end
$$;

drop trigger if exists games_resolve_opponent_team on public.games;
create trigger games_resolve_opponent_team
  before insert or update on public.games
  for each row execute function public.resolve_game_opponent_team();

drop trigger if exists games_sync_league_pair on public.games;
create trigger games_sync_league_pair
  after insert or update on public.games
  for each row execute function public.sync_game_league_pair();

drop trigger if exists games_delete_league_pair on public.games;
create trigger games_delete_league_pair
  after delete on public.games
  for each row execute function public.sync_game_league_pair();
