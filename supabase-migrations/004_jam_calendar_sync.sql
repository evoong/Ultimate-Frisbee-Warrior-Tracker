-- authenticate via its own CRON_SECRET convention (a plain Vercel env var
-- Vercel reads and auto-attaches as "Authorization: Bearer <value>" to cron
-- requests -- see https://vercel.com/docs/cron-jobs/manage-cron-jobs#securing-cron-jobs),
-- not Vault. Cloudflare's scheduled() trigger calls the sync function
-- in-process and isn't reachable over HTTP at all, so it needs no secret.
